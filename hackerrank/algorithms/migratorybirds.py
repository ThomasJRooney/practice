def migratoryBirds(arr):
    tmp = [0] * (max(arr) + 1)
    for i in range(len(arr)):
        tmp[arr[i]] += 1
    m = max(tmp)
    for i in range(len(tmp)):
        if tmp[i] == m:
            return i

